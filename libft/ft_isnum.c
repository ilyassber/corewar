#include "libft.h"

int	ft_isnum(char *str)
{
	int	i;

	i = 0;
	while (str && str[i] != '\0')
	{
		if (i == 0 && (str[i] == '-' || str[i] == '+'))
			i++;
		else if (ft_isdigit(str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
